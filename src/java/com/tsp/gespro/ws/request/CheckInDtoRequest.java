package com.tsp.gespro.ws.request;

import java.util.Date;

/**
 *
 * @author HpPyme
 */
public class CheckInDtoRequest {   
      
  private int idRegistro;  
  private int idCliente;
  private Date fechaHora;
  private int idTipoCheck;//1 Entrada, 2 Salida
  private int idDetalleCheck; // 0 Sin detalle, 1 Comida, 2 Descanso, 3 Permiso, 4 Fin de jornada, 5 Otro
  private String comentarios;
  private double latitud;
  private double longitud;
  private int idEstatus; //1 Activo, 2 Inactivo

    public int getIdRegistro() {
        return idRegistro;
    }

    public void setIdRegistro(int idRegistro) {
        this.idRegistro = idRegistro;
    }  
  
    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public Date getFechaHora() {
        return fechaHora;
    }

    public void setFechaHora(Date fechaHora) {
        this.fechaHora = fechaHora;
    }

    public int getIdTipoCheck() {
        return idTipoCheck;
    }

    public void setIdTipoCheck(int idTipoCheck) {
        this.idTipoCheck = idTipoCheck;
    }

    public int getIdDetalleCheck() {
        return idDetalleCheck;
    }

    public void setIdDetalleCheck(int idDetalleCheck) {
        this.idDetalleCheck = idDetalleCheck;
    }

    public String getComentarios() {
        return comentarios;
    }

    public void setComentarios(String comentarios) {
        this.comentarios = comentarios;
    }

    public double getLatitud() {
        return latitud;
    }

    public void setLatitud(double latitud) {
        this.latitud = latitud;
    }

    public double getLongitud() {
        return longitud;
    }

    public void setLongitud(double longitud) {
        this.longitud = longitud;
    }

    public int getIdEstatus() {
        return idEstatus;
    }

    public void setIdEstatus(int idEstatus) {
        this.idEstatus = idEstatus;
    }
    
  
  
    
}
